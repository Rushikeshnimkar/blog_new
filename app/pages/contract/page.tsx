const RegisterWifiNode: React.FC = () => {
  const [registration, setRegistration] = useState<WifiNodeRegistration>({
    user: '',
    deviceId: '',
    peaqDid: '',
    ssid: '',
    location: '',
    pricePerMin: 0,
  });
  const [txHash, setTxHash] = useState<string | null>(null);
  const [error, setError] = useState<string | null>(null);

  const handleInputChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    const { name, value } = e.target;
    setRegistration(prev => ({ ...prev, [name]: value }));
  };

  const callDelegateRegisterWifiNode = async (params: WifiNodeRegistration) => {
    try {
      const { user, deviceId, peaqDid, ssid, location, pricePerMin } = params;
  
      if (typeof window.ethereum === 'undefined') {
        throw new Error('Ethereum provider not found. Please install MetaMask.');
      }
  
      // Connect to the Ethereum provider (e.g., MetaMask)
      const provider = new ethers.BrowserProvider(window.ethereum);
      
      // Check the network
      const network = await provider.getNetwork();
      console.log('Connected to network:', network.chainId.toString());
  
      // Request account access if needed
      await provider.send('eth_requestAccounts', []);
      
      // Get the signer
      const signer = await provider.getSigner();
  
      // Ensure all addresses are valid
      if (!ethers.isAddress(contractAddress)) {
        throw new Error('Invalid contract address');
      }
      if (!ethers.isAddress(user)) {
        throw new Error('Invalid user address');
      }
      if (!ethers.isAddress(peaqDid)) {
        throw new Error('Invalid peaqDid address');
      }
  
      // Create the transaction data manually
      const iface = new ethers.Interface(contractABI);
      const data = iface.encodeFunctionData('delegateRegisterWifiNode', [
        user,
        deviceId,
        peaqDid,
        ssid,
        location,
        ethers.parseUnits(pricePerMin.toString(), 'ether')
      ]);
  
      // Create and send the transaction
      const tx = await signer.sendTransaction({
        to: contractAddress,
        data: data,
      });
  
      // Wait for the transaction to be mined
      await tx.wait();
      
      return tx.hash;
    } catch (error) {
      console.error('Error calling delegateRegisterWifiNode:', error);
      throw error;
    }
  };
  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setError(null);
    setTxHash(null);

    try {
      const hash = await callDelegateRegisterWifiNode(registration);
      setTxHash(hash);
    } catch (err) {
      setError(err instanceof Error ? err.message : 'An unknown error occurred');
    }
  };

  return (
    <div>
      <h2>Register WiFi Node</h2>
      <form onSubmit={handleSubmit}>
        <input
          name="user"
          value={registration.user}
          onChange={handleInputChange}
          placeholder="User Address"
          required
        />
        <input
          name="deviceId"
          value={registration.deviceId}
          onChange={handleInputChange}
          placeholder="Device ID"
          required
        />
        <input
          name="peaqDid"
          value={registration.peaqDid}
          onChange={handleInputChange}
          placeholder="Peaq DID"
          required
        />
        <input
          name="ssid"
          value={registration.ssid}
          onChange={handleInputChange}
          placeholder="SSID"
          required
        />
        <input
          name="location"
          value={registration.location}
          onChange={handleInputChange}
          placeholder="Location"
          required
        />
        <input
          type="number"
          name="pricePerMin"
          value={registration.pricePerMin}
          onChange={handleInputChange}
          placeholder="Price Per Minute (ETH)"
          required
          step="0.000001"
        />
        <button type="submit">Register</button>
      </form>
      {txHash && <p>Registration successful! Transaction hash: {txHash}</p>}
      {error && <p style={{ color: 'red' }}>Error: {error}</p>}
    </div>
  );
};

export default RegisterWifiNode;